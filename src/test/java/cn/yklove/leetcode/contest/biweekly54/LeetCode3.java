    class Solution {
        public int largestMagicSquare(int[][] grid) {
            int[][] y = new int[grid.length][grid[0].length];
            int[][] x = new int[grid.length][grid[0].length];
            int[][] right = new int[grid.length][grid[0].length];
            int[][] left = new int[grid.length][grid[0].length];
            for (int i = 0; i < grid.length; i++) {
                int[] gridX = grid[i];
                for (int j = 0; j < gridX.length; j++) {
                    if (i == 0) {
                        y[i][j] = gridX[j];
                    } else {
                        y[i][j] = gridX[j] + y[i - 1][j];
                    }
                }
            }
            for (int i = 0; i < grid[0].length; i++) {
                for (int j = 0; j < grid.length; j++) {
                    if (i == 0) {
                        x[j][i] = grid[j][i];
                    } else {
                        x[j][i] = x[j][i - 1] + grid[j][i];
                    }
                }
            }
            for (int i = 0; i < grid.length; i++) {
                for (int j = 0; j < grid[0].length; j++) {
                    if (i - 1 >= 0 && j - 1 >= 0) {
                        right[i][j] = grid[i][j] + right[i - 1][j - 1];
                    } else {
                        right[i][j] = grid[i][j];
                    }
                }
            }
            for (int i = 0; i < grid.length; i++) {
                for (int j = grid[0].length - 1; j >= 0; j--) {
                    if (i - 1 >= 0 && j + 1 < grid[0].length) {
                        left[i][j] = grid[i][j] + left[i - 1][j + 1];
                    } else {
                        left[i][j] = grid[i][j];
                    }
                }
            }
            int maxK = 1;
            for (int i = 0; i < grid.length; i++) {
                for (int j = 0; j < grid[i].length; j++) {
                    // 遍历k
                    for (int k = 1; i + k < grid.length && j + k < grid[i].length; k++) {
                        // 检查
                        int tmp1 = y[i + k][j] - ((i - 1) < 0 ? 0 : y[i - 1][j]);
                        int tmp2 = x[i][j + k] - ((j - 1) < 0 ? 0 : x[i][j - 1]);
                        int tmp3 = right[i + k][j + k] - ((i - 1 < 0 || (j - 1) < 0) ? 0 : right[i - 1][j - 1]);
                        int tmp4 = left[i + k][j] - (((j + k + 1) >= grid[i].length || i - 1 < 0) ? 0 : left[i - 1][j + k + 1]);
                        if (tmp1 == tmp2 && tmp2 == tmp3 && tmp3 == tmp4) {
                            boolean key = true;
                            for (int l = 1; l < k; l++) {
                                int tmp1X = y[i + k][j + l] - ((i - 1) < 0 ? 0 : y[i - 1][j + l]);
                                if (tmp1X != tmp1) {
                                    key = false;
                                    break;
                                }
                                int tmp2X = x[i + l][j + k] - ((j - 1) < 0 ? 0 : x[i + l][j - 1]);
                                if (tmp2X != tmp2) {
                                    key = false;
                                    break;
                                }
                            }
                            if (key) {
                                maxK = Math.max(maxK, k + 1);
                                maxK = maxK;
                            }
                        }
                    }
                }
            }
            return maxK;
        }
    }

    Solution solution = new Solution();

