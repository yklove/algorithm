    public void test() {
        Assert.assertEquals(3, solution.largestMagicSquare(new int[][]{
                new int[]{7, 1, 4, 5, 6},
                new int[]{2, 5, 1, 6, 4},
                new int[]{1, 5, 4, 3, 2},
                new int[]{1, 2, 7, 3, 4}
        }));
    }

